import math
Pi = math.pi

#taking radius input form user
radius= float(input("Enter the radius of circle "))
#valvulating area value
area = Pi * radius ** 2

#getting type of a pi, radius and area
print("The type of Pi, radius are ", type(Pi), type(radius), type(area))

#Printing the value of radius
print("The radius is: ", radius)

#printing the value of area
print("The area of circle is ", area)


